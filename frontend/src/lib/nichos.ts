/** Limite espelhado do backend (MAX_NICHOS_BUSCA_MAPA). */
export const LIMITE_NICHOS_POR_BUSCA = 15

// faixa dos acentos combinantes do Unicode (NFD separa "é" em "e" + acento)
const REGEX_ACENTOS_COMBINANTES = /[̀-ͯ]/g

export function normalizarParaBusca(texto: string): string {
  return texto.normalize("NFD").replace(REGEX_ACENTOS_COMBINANTES, "").toLowerCase()
}

